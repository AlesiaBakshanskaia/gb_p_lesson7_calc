def oper_add(num_1, num_2):
    return num_1 + num_2


def oper_sub(num_1, num_2):
    return num_1 - num_2


def oper_mult(num_1, num_2):
    return num_1 * num_2


def oper_simp_div(num_1, num_2):
    return num_1 / num_2


def oper_div(num_1, num_2):
    return num_1 // num_2

    
def oper_mod(num_1, num_2):
    return num_1 % num_2


def oper_exp(num_1, num_2):
    return num_1 ** num_2